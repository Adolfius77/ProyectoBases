    private void BtnAgregarDirectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarDirectorActionPerformed
        String idDirector = txtIdDirector.getText();
        String nombre = txtNombre.getText();
        String nacionalidad = txtNacionalidad.getText();
        
        if (!idDirector.isEmpty() && !nombre.isEmpty() && !nacionalidad.isEmpty()) {
            
            dirrectorDTO nuevoDirrector = new dirrectorDTO(0, nombre, nacionalidad);
            directoresDAO agregarDirectorDAO = new directoresDAO();
            
            if (agregarDirectorDAO.agregarDirector(nuevoDirrector)) {
                JOptionPane.showMessageDialog(this, "Productora agregada correctamente");
                    txtIdDirector.setText("");
                    txtNombre.setText("");
                    txtNacionalidad.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Error al agregar el dirrector. Verifique los datos o la consola para m√°s detalles.");
                }
        }
    }//GEN-LAST:event_BtnAgregarDirectorActionPerformed


